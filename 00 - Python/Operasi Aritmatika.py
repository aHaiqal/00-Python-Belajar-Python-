a = 10
b = 3

# Operasi penjumlahan +
hasil = a + b
print(a,'+',b,'=',hasil)

# Operasi pengurangan -
hasil = a - b
print(a,'-',b,'=',hasil)

# Operasi perkalian *
hasil = a * b
print(a,'*',b,'=',hasil)

# Operasi pembagian /
hasil = a / b
print(a,'/',b,'=',hasil)

# Operasi eksponen **
hasil = a ** b
print(a,'**',b,'=',hasil)

# Operasi modulus (sisa pembagian) (%)
hasil = a % b
print(a,'%',b,"=",hasil)

# Operasi floor division //
hasil = a // b
print(a,'//',b,'=',hasil)

# Prioritas operasi, operational precedence
# Urutan perhitungan #(), eksponen, perkalian, pertambahan
x = 2
y = 3
z = 4

hasil = (x + y) * z
print('(',x,'+',y,') *',z,'=',hasil)

